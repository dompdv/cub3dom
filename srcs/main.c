float absolute_value(float value)
{
	if (value >= 0)
		return (value);
	return (-value);
}

void my_mlx_pixel_put(t_data *data, int x, int y, int color)
{
	char *dst;

	dst = data->addr + (y * data->line_length + x * (data->bits_per_pixel / 8));
	*(unsigned int *)dst = color;
}

int create_trgb(int t, int r, int g, int b)
{
	return (t << 24 | r << 16 | g << 8 | b);
}

char get_color_name(int color)
{
	int r;
	int g;
	int b;

	r = (color >> 16 & 0xFF);
	g = (color >> 8 & 0xFF);
	b = (color & 0xFF);
	if (r == 255 && g == 0 && b == 0)
		return ('R');
	if (r > 0 && g == 0 && b == 0)
		return ('r');
	if (r == 0 && g == 255 && b == 0)
		return ('G');
	if (r == 0 && g > 0 && b == 0)
		return ('g');
	if (r == 0 && g == 0 && b == 255)
		return ('B');
	if (r == 0 && g == 0 && b > 0)
		return ('b');
	if (r == 255 && g == 255 && b == 255)
		return ('W');
	if (r > 0 && g > 0 && b > 0)
		return ('w');
	return ('y');
}

void draw_vertical_line(t_data *data, int x, int draw_start, int draw_end, int color)
{
	int y;
	y = draw_start;
	while (y < draw_end)
	{
		my_mlx_pixel_put(data, x, y, color);
		y++;
	}
}

int get_darker(int color)
{
	int r;
	int g;
	int b;

	r = (color >> 16 & 0xFF) / 2;
	g = (color >> 8 & 0xFF) / 2;
	b = (color & 0xFF) / 2;
	return (create_trgb(0, r, g, b));
}

int raycast(t_world world)
{
	t_data img;

	img.img = mlx_new_image(world.mlx, SCREEN_WIDTH, SCREEN_HEIGHT);
	img.addr = mlx_get_data_addr(img.img, &img.bits_per_pixel, &img.line_length, &img.endian);
	//	draw_vertical_line(&img, x, draw_start, draw_stop, color);
	mlx_put_image_to_window(world.mlx, world.window.reference, img.img, 0, 0);
	return (0);
}

